    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jComboBox1.getSelectedIndex() == 0) {
            new Thread() {
                @Override
                public void run() {
                    while (true) {
                        String entrada = con.read();
                        if (entrada != null) {
                            if (entrada.equals(valorLigar) && !ligou) {
                                    con.send("L");
                                    ligou = true;
                            }
                        }
                     try {
                            Thread.sleep(1000);
                        } catch (InterruptedException ex) {
                            Logger.getLogger(telaConfControle.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                }
            }.start();
        }else if(jComboBox1.getSelectedIndex() == 1){
            new Thread() {
                @Override
                public void run() {
                    while (true) {
                        String entrada = con.read();
                        if (entrada != null) {
                            if (entrada.equals(valorDesligar) && ligou) {
                                con.send("D");
                                ligou = false;
                            }
                        }
                     try {
                            Thread.sleep(1000);
                        } catch (InterruptedException ex) {
                            Logger.getLogger(telaConfControle.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                }
            }.start();
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed
   
